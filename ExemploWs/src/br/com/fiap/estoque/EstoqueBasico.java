package br.com.fiap.estoque;

public class EstoqueBasico {

	public int soma (int nr1, int nr2) {
		return (nr1+nr2);
	}
	
	public int subtrair (int nr1, int nr2) {
		return (nr1-nr2);
	}
	
	public int multiplicar (int nr1, int nr2) {
		return (nr1*nr2);
	}
	
	public int dividir (int nr1, int nr2) {
		return (nr1/nr2);
	}
	
}
